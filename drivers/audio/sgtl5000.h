// CHIP_PLL_CTRL Register
#define SGTL5000_PLL_CTRL 0x0032

// This is the integer portion of the PLL divisor. To determine the value of this
// field use the following calculations: INT_DIVISOR =
// FLOOR(PLL_OUTPUT_FREQ/INPUT_FREQ). PLL_OUTPUT_FREQ = 180.6336 MHz if system
// sample rate = 44.1 Khz else PLL_OUTPUT_FREQ = 196.608 MHz if system sample rate
// != 44.1 KHz. INPUT_FREQ = frequency of the external MCLK provided if
// CHIP_CLK_TOP_CTRL_INPUT_FREQ_DIV2 = 0x0 else INPUT_FREQ = (frequency of the
// external MCLK provided / 2) if CHIP_CLK_TOP_CTRL_INPUT_FREQ_DIV2 = 0x1. (Reset:
// 0xA)
#define SGTL5000_PLL_CTRL_INT_DIVISOR_MASK  0xF800
#define SGTL5000_PLL_CTRL_INT_DIVISOR_SHIFT 11
#define SGTL5000_PLL_CTRL_INT_DIVISOR_SET(x) (((x) << SGTL5000_PLL_CTRL_INT_DIVISOR_SHIFT) & SGTL5000_PLL_CTRL_INT_DIVISOR_MASK)
#define SGTL5000_PLL_CTRL_INT_DIVISOR_DEFAULT 0xA

// This is the fractional portion of the PLL divisor. To determine the value of
// this field use the following calculation: FRAC_DIVISOR =
// ((PLL_OUTPUT_FREQ/INPUT_FREQ) - INT_DIVISOR) * 2048. PLL_OUTPUT_FREQ = 180.6336
// MHz if system sample rate = 44.1 KHz else PLL_OUTPUT_FREQ = 190.608 MHz if
// system sample rate != 44.1 KHz. INPUT_FREQ = frequency of the external MCLK
// provided if CHIP_CLK_TOP_CTRL_INPUT_FREQ_DIV2 = 0x0 else INPUT_FREQ = (frequency
// of the external MCLK provided / 2) if CHIP_CLK_TOP_CTRL_INPUT_FREQ_DIV2 = 0x1
// (Reset: 0x0)
#define SGTL5000_PLL_CTRL_FRAC_DIVISOR_MASK  0x07FF
#define SGTL5000_PLL_CTRL_FRAC_DIVISOR_SHIFT 0
#define SGTL5000_PLL_CTRL_FRAC_DIVISOR_SET(x) (((x) << SGTL5000_PLL_CTRL_FRAC_DIVISOR_SHIFT) & SGTL5000_PLL_CTRL_FRAC_DIVISOR_MASK)
